function setLanguage(lang) {
    localStorage.setItem('uk_lang', lang);
    document.documentElement.lang = lang;

    // Update all static elements with data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            let text = translations[lang][key];

            // Dynamic Year Replacement
            if (key === 'footer_text') {
                text = text.replace('{year}', new Date().getFullYear());
            }

            // Handle input placeholders specifically
            if (el.tagName === 'INPUT') {
                el.placeholder = text;
            } else {
                el.innerHTML = text;
            }
        }
    });

    // Update global lang
    window.currentLanguage = lang;

    // Trigger custom event for dynamic content
    const event = new CustomEvent('languageChanged', { detail: { language: lang } });
    window.dispatchEvent(event);
}

// Initial load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('uk_lang') || 'en';

    // Set desktop dropdown value if it exists
    const desktopDropdown = document.getElementById('desktop-lang');
    if (desktopDropdown) {
        desktopDropdown.value = savedLang;
    }

    // Set mobile dropdown value if it exists
    const mobileDropdown = document.getElementById('mobile-lang');
    if (mobileDropdown) {
        mobileDropdown.value = savedLang;
    }

    setLanguage(savedLang);
});
